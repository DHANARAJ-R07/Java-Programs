class switchstatement {
    public static void main(String[] args) {
        int a = 5;
        switch (a) {
            case 3:
                System.out.println("a is 3");
                break;
            case 5:
                System.out.println("a is 5");
                break;
            case 7:
                System.out.println("a is 7");
                break;
            case 10:
                System.out.println("a is 10");
                break;
            default:
                System.out.println("Invalid day");
        }
    }

}
